def build_graph(shlokas):
    G = nx.Graph()
    for sl in shlokas:
        node_id = f"1.{sl['shloka_number']}"
        G.add_node(node_id, shloka=sl, topics=set(sl['topics']))
    nodes = list(G.nodes(data=True))
    for i in range(len(nodes)):
        id1, data1 = nodes[i]
        topics1 = data1['topics']
        for j in range(i+1, len(nodes)):
            id2, data2 = nodes[j]
            topics2 = data2['topics']
            common = topics1 & topics2
            if common:
                G.add_edge(id1, id2, weight=len(common), common=common)
    return G

