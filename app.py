def word_overlap_score(query, shloka):
    q_tokens = set(tokenize_gujarati(query))
    s_tokens = shloka['_tokens']
    if not q_tokens or not s_tokens:
        return 0
    return len(q_tokens & set(s_tokens))

def find_shlokas(query, G, shlokas, top_k=3):
    # Manual override for father-related queries
    if any(word in query.lower() for word in ["પિતા", "પિતૃ", "બાપ", "પિતાની", "પિતાને"]):
        result = []
        for sl in shlokas:
            if sl['shloka_number'] in [26, 35]:
                result.append(sl)
        if result:
            expanded = set()
            for sl in result:
                node_id = f"1.{sl['shloka_number']}"
                expanded.add(node_id)
                if node_id in G:
                    for nb in G.neighbors(node_id):
                        expanded.add(nb)
            final = []
            for n in expanded:
                final.append(G.nodes[n]['shloka'])
            return final[:top_k+2]

    q_topics = extract_query_topics(query)
    if q_topics:
        scores = []
        for node in G.nodes(data=True):
            node_id, data = node
            common = len(q_topics & data['topics'])
            if common > 0:
                scores.append((node_id, common))
        scores.sort(key=lambda x: x[1], reverse=True)
        top_nodes = [s[0] for s in scores[:top_k]]
        if top_nodes:
            expanded = set(top_nodes)
            for n in top_nodes:
                for nb in G.neighbors(n):
                    expanded.add(nb)
            result_nodes = list(expanded)[:top_k+2]
            return [G.nodes[n]['shloka'] for n in result_nodes]
    
    # Fallback: word overlap (penalise shlokas 1-3)
    scored = []
    for sl in shlokas:
        score = word_overlap_score(query, sl)
        if sl['shloka_number'] in [1,2,3]:
            score = score * 0.1
        if score > 0:
            scored.append((sl, score))
    scored.sort(key=lambda x: x[1], reverse=True)
    if scored:
        return [sl for sl, _ in scored[:top_k]]
    else:
        return [sl for sl in shlokas if sl['shloka_number'] in [26,35,28]][:top_k]

